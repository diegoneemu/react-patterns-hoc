export function getBlogPost(id) {
  return BlogPosts.filter(BlogPosts => BlogPosts.id === id);
}

export function addChangeListener(handler) {
  BlogPosts.addEventListener("onChange", handler);
}

export function removeChangeListener(handler) {
  BlogPosts.removeEventListener("onChange", handler);
}
